
# using 'int' to cast string into an interger

number = int(input("Enter a number:  "))
double_number = number * 2
print(double_number)  # note the print. There is no decimal. The number is a real number


# use of eval() ---- try entering a float - i.e. 1.5
number = eval(input("Enter a number:  "))
double_number = number * 2
print(double_number)   # note the print. eval has a number after the decimal


result = eval('1+1+2+3+4+5+5')
print(result)   # result = 21






